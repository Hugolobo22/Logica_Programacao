# Lista de temperaturas
T = [-10, -8, 0, 1, 2, 5, -2, -4]

# Encontrar a menor temperatura
min_temperatura = min(T)

# Encontrar a maior temperatura
max_temperatura = max(T)

# Calcular a temperatura média
media_temperatura = sum(T) / len(T)

# Imprimir os resultados
print("Menor temperatura:", min_temperatura)
print("Maior temperatura:", max_temperatura)
print("Temperatura média:", media_temperatura)