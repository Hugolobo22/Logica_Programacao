# Primeiramente atribuimos o seu nome para a variável "nome" através do comando input
nome = input("Qual o seu nome?")

# Após isso, inserimos a sua idade através do mesmo comando
idade = input("Qual a sua idade?")

# Para finalizar, mostraremos através do "print" o seu nome e sua idade na tela
print("seu nome é", nome, "e você tem",idade,"anos!")

