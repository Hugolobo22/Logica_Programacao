# Primeiro atribuo o valor da variável macas (maçãs) e utilizo print para mostrar o valor na tela
macas = 5
print("quantidade de maçãs é", macas)

# Após isso, atribuo o valor da variável laranjas e utilizo novamente o print para mostrar o valor na tela
laranjas = 10
print("quantidade de laranjas é", laranjas)

# Crio uma variável frutas para mostrar o valor de todas as frutas juntas
frutas = (macas + laranjas)
print("quantidade de frutas é", frutas)

# Reatribuo o valor de maçãs e utilizo o print para mostrar na tela
macas = 20
print("nova quantidade de maçãs é", macas)

# Recalculo o valor de frutas devido à alteração do valor de maçãs e utilizo print para mostrar o resultado
frutas = (macas + laranjas)
print("nova quantidade de frutas é", frutas)